package tabelas;

public class Cidades {
    
    int Codigo_Cidades;
    String Nome_Cidades;
    int Codigo_Estados;

    public int getCodigo_Cidades() {
        return Codigo_Cidades;
    }

    public void setCodigo_Cidades(int Codigo_Cidades) {
        this.Codigo_Cidades = Codigo_Cidades;
    }

    public String getNome_Cidades() {
        return Nome_Cidades;
    }

    public void setNome_Cidades(String Nome_Cidades) {
        this.Nome_Cidades = Nome_Cidades;
    }

    public int getCodigo_Estados() {
        return Codigo_Estados;
    }

    public void setCodigo_Estados(int Codigo_Estados) {
        this.Codigo_Estados = Codigo_Estados;
    }
    
}
