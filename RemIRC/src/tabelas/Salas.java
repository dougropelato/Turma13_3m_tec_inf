package tabelas;

/**
 *
 * @author M5L2
 */
public class Salas {
    
   private int codigo_salas;
   private String nome_salas;
   private int ip_salas;
   private int porta_salas;

    /**
     * @return the codigo_salas
     */
    public int getCodigo_salas() {
        return codigo_salas;
    }

    /**
     * @param codigo_salas the codigo_salas to set
     */
    public void setCodigo_salas(int codigo_salas) {
        this.codigo_salas = codigo_salas;
    }

    /**
     * @return the nome_salas
     */
    public String getNome_salas() {
        return nome_salas;
    }

    /**
     * @param nome_salas the nome_salas to set
     */
    public void setNome_salas(String nome_salas) {
        this.nome_salas = nome_salas;
    }

    /**
     * @return the ip_salas
     */
    public int getIp_salas() {
        return ip_salas;
    }

    /**
     * @param ip_salas the ip_salas to set
     */
    public void setIp_salas(int ip_salas) {
        this.ip_salas = ip_salas;
    }

    /**
     * @return the porta_salas
     */
    public int getPorta_salas() {
        return porta_salas;
    }

    /**
     * @param porta_salas the porta_salas to set
     */
    public void setPorta_salas(int porta_salas) {
        this.porta_salas = porta_salas;
    }
    
}
