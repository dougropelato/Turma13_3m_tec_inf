package tabelas;

public class Estados {
    
    private int Codigo_estados;
    private String Nome_estados;
    private String Sigla_estados;
    
 
    public int getCodigo_estados() {
        return Codigo_estados;
    }

    public void setCodigo_estados(int codigo_estados) {
        this.Codigo_estados = codigo_estados;
    }

    public String getNome_estados() {
        return Nome_estados;
    }

    public void setNome_estados(String nome_estados) {
        this.Nome_estados = nome_estados;
    }

    public String getSigla_estados() {
        return Sigla_estados;
    }

    public void setSigla_estados(String sigla_estados) {
        this.Sigla_estados = sigla_estados;
    }
    
}
